cc.Class({
    extends: cc.Component,

    properties: {
        moveSpeed : 9,
    },

    // LIFE-CYCLE CALLBACKS:

    onLoad () {},

    start () {

    },

    update (dt) {
        this.node.x -= this.moveSpeed;
        if(this.node.x <= -this.node.parent.width / 2){
            this.node.x = this.node.parent.width / 2;
        }
    },
});
